import { mkdirSync, writeFileSync } from "node:fs";
import { dirname, resolve } from "node:path";
import { APP_SCOPE_KEY, buildSeedData } from "./seed-app-content-shared.mjs";

function escapeSqlString(value) {
  return String(value).replace(/'/g, "''");
}

function sqlText(value) {
  return `'${escapeSqlString(value)}'`;
}

function sqlNullableText(value) {
  if (value == null) return "NULL";
  return sqlText(value);
}

function sqlBool(value) {
  return value ? "TRUE" : "FALSE";
}

function formatTuple(values) {
  return `(${values.join(", ")})`;
}

function buildSql() {
  const { tags, bookmarks } = buildSeedData();
  const tagIdByKey = new Map(tags.map((tag) => [tag.key, tag.id]));

  const bookmarkTags = bookmarks.flatMap((bookmark) => (
    bookmark.tagKeys.map((tagKey) => {
      const tagId = tagIdByKey.get(tagKey);
      if (!tagId) {
        throw new Error(`Missing tag id for key: ${tagKey}`);
      }
      return {
        bookmarkId: bookmark.id,
        tagId
      };
    })
  ));

  const tagValues = tags.map((tag) => formatTuple([
    sqlText(tag.id),
    sqlText("APP"),
    "NULL",
    sqlText(tag.name),
    sqlNullableText(tag.color),
    sqlNullableText(tag.description),
    String(tag.sortOrder),
    "0",
    sqlText(APP_SCOPE_KEY),
    "NOW()",
    "NOW()"
  ]));

  const bookmarkValues = bookmarks.map((bookmark) => formatTuple([
    sqlText(bookmark.id),
    sqlText("APP"),
    "NULL",
    sqlText(bookmark.title),
    sqlText(bookmark.url),
    sqlText(bookmark.normalizedUrl),
    sqlNullableText(bookmark.description),
    sqlBool(bookmark.isFavorite),
    sqlBool(bookmark.isVisible),
    sqlText(APP_SCOPE_KEY),
    sqlText(bookmark.createdAt),
    sqlText(bookmark.createdAt)
  ]));

  const bookmarkTagValues = bookmarkTags.map((row) => formatTuple([
    sqlText(row.bookmarkId),
    sqlText(row.tagId),
    "NOW()"
  ]));

  const lines = [];
  lines.push("-- Generated by scripts/seed-data/generate-seed-app-content-sql.mjs");
  lines.push("-- Source data: scripts/seed-data/app-initial-content.mjs");
  lines.push("");
  lines.push("BEGIN;");
  lines.push("");
  lines.push("-- Reset APP scope bookmarks/tags.");
  lines.push('DELETE FROM "bookmark_tags" bt');
  lines.push('USING "bookmarks" b');
  lines.push('WHERE bt."bookmark_id" = b."id"');
  lines.push("  AND b.\"scope\" = 'APP';");
  lines.push("");
  lines.push('DELETE FROM "bookmark_tags" bt');
  lines.push('USING "tags" t');
  lines.push('WHERE bt."tag_id" = t."id"');
  lines.push("  AND t.\"scope\" = 'APP';");
  lines.push("");
  lines.push('DELETE FROM "bookmarks" WHERE "scope" = \'APP\';');
  lines.push('DELETE FROM "tags" WHERE "scope" = \'APP\';');
  lines.push("");
  lines.push('INSERT INTO "tags" (');
  lines.push('  "id",');
  lines.push('  "scope",');
  lines.push('  "owner_user_id",');
  lines.push('  "name",');
  lines.push('  "color",');
  lines.push('  "description",');
  lines.push('  "sort_order",');
  lines.push('  "bookmark_count",');
  lines.push('  "scope_owner_key",');
  lines.push('  "created_at",');
  lines.push('  "updated_at"');
  lines.push(")");
  lines.push("VALUES");
  lines.push(`  ${tagValues.join(",\n  ")};`);
  lines.push("");
  lines.push('INSERT INTO "bookmarks" (');
  lines.push('  "id",');
  lines.push('  "scope",');
  lines.push('  "owner_user_id",');
  lines.push('  "title",');
  lines.push('  "url",');
  lines.push('  "normalized_url",');
  lines.push('  "description",');
  lines.push('  "is_favorite",');
  lines.push('  "is_visible",');
  lines.push('  "scope_owner_key",');
  lines.push('  "created_at",');
  lines.push('  "updated_at"');
  lines.push(")");
  lines.push("VALUES");
  lines.push(`  ${bookmarkValues.join(",\n  ")};`);
  lines.push("");
  lines.push('INSERT INTO "bookmark_tags" (');
  lines.push('  "bookmark_id",');
  lines.push('  "tag_id",');
  lines.push('  "created_at"');
  lines.push(")");
  lines.push("VALUES");
  lines.push(`  ${bookmarkTagValues.join(",\n  ")}`);
  lines.push('ON CONFLICT ("bookmark_id", "tag_id") DO NOTHING;');
  lines.push("");
  lines.push("WITH recount AS (");
  lines.push("  SELECT");
  lines.push('    t."id" AS "tag_id",');
  lines.push('    COUNT(bt."bookmark_id")::int AS "cnt"');
  lines.push('  FROM "tags" t');
  lines.push('  LEFT JOIN "bookmark_tags" bt ON bt."tag_id" = t."id"');
  lines.push("  WHERE t.\"scope\" = 'APP'");
  lines.push('  GROUP BY t."id"');
  lines.push(")");
  lines.push('UPDATE "tags" t');
  lines.push('SET "bookmark_count" = recount."cnt", "updated_at" = NOW()');
  lines.push("FROM recount");
  lines.push('WHERE t."id" = recount."tag_id";');
  lines.push("");
  lines.push("COMMIT;");
  lines.push("");

  return {
    sql: lines.join("\n"),
    stats: {
      tagCount: tags.length,
      bookmarkCount: bookmarks.length,
      bookmarkTagCount: bookmarkTags.length
    }
  };
}

function main() {
  const outputArg = process.argv[2];
  const outputPath = resolve(outputArg ?? "scripts/seed-data/seed-app-content.generated.sql");

  const { sql, stats } = buildSql();
  mkdirSync(dirname(outputPath), { recursive: true });
  writeFileSync(outputPath, sql, "utf8");

  console.log(`SQL generated: ${outputPath}`);
  console.log(`Counts: tags=${stats.tagCount}, bookmarks=${stats.bookmarkCount}, bookmarkTags=${stats.bookmarkTagCount}`);
}

main();
